def add(num1, num2):
    print('Addition: %d' % (num1 + num2))

def subtract(num1, num2):
    print('Subtraction: %d' % (abs(num1 - num2)))

def multiply(num1, num2):
    print('Multiplication: %d' % (num1 * num2))

def divide(num1, num2):
    print('Division: %d' % (num1 / num2))

add(2, 2) # 4
subtract(2, 10) # 8
multiply(5, 3) # 15
divide(10, 2) # 5